#include "ft_printf.h"

size_t	fsize(unsigned long long n, int base)
{
	int i;

	i = 0;
	if (n == 0)
		return (1);
	else
	{
		while (n)
		{
			n /= base;
			i++;
		}
	}
	return (i);
}
