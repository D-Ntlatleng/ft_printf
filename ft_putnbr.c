#include "ft_printf.h"

void	ft_putnbr(long long n, t_flag *my)
{
	if (n == LONG_MIN)
	{
		ft_putstr("-9223372036854775808", my);
		return ;
	}
	if (n < 0)
		n *= -1;
	if (n > 10)
		ft_putnbr(n / 10, my);
	ft_putchar(n % 10 + 48, my);
}
